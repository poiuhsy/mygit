package com.meetin.dto;

public class Meeting {
	int meeting_sq;
	String meeting_cate;
	String meeting_nm;
	String meeting_dt;
	int meeting_fl;
	
	public int getMeeting_sq() {
		return meeting_sq;
	}
	public void setMeeting_sq(int meeting_sq) {
		this.meeting_sq = meeting_sq;
	}
	public String getMeeting_cate() {
		return meeting_cate;
	}
	public void setMeeting_cate(String meeting_cate) {
		this.meeting_cate = meeting_cate;
	}
	public String getMeeting_nm() {
		return meeting_nm;
	}
	public void setMeeting_nm(String meeting_nm) {
		this.meeting_nm = meeting_nm;
	}
	public String getMeeting_dt() {
		return meeting_dt;
	}
	public void setMeeting_dt(String meeting_dt) {
		this.meeting_dt = meeting_dt;
	}
	public int getMeeting_fl() {
		return meeting_fl;
	}
	public void setMeeting_fl(int meeting_fl) {
		this.meeting_fl = meeting_fl;
	}
	
	
}
